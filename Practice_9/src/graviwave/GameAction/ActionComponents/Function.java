package graviwave.GameAction.ActionComponents;

public class Function {
    private double k, l, p;
    Function(double k, double l, double p){

        this.k = k;
        this.l = l;
        this.p = p;
    }

    double getK() {
        return k;
    }

    double getL() {
        return l;
    }

    double getP() {
        return p;
    }
}
